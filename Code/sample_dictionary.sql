CREATE TABLE IPASymbol (SID INTEGER, Symbol CHAR(1) NOT NULL, UNIQUE (Symbol), PRIMARY KEY (SID));
CREATE TABLE Vowel (SID INTEGER, Rounding VARCHAR(20), Backness VARCHAR(20), Height VARCHAR(20), UNIQUE (Rounding, Backness, Height), PRIMARY KEY (SID), FOREIGN KEY (SID) REFERENCES IPASymbol (SID));
CREATE TABLE Consonant (SID INTEGER, Articulation_place VARCHAR(20), Articulation_manner VARCHAR(20), Voicing VARCHAR(20), UNIQUE (Articulation_place, Articulation_manner, Voicing), PRIMARY KEY (SID), FOREIGN KEY (SID) REFERENCES IPASymbol (SID));
CREATE TABLE Pronunciation (IPA_transcription VARCHAR(20), Phonetic_spelling VARCHAR(50), UNIQUE (Phonetic_spelling), PRIMARY KEY (IPA_transcription));
CREATE TABLE Language_family (Family VARCHAR(20), Ancestor VARCHAR(20), PRIMARY KEY (Ancestor));
CREATE TABLE Language_ancestor (Name VARCHAR(20), Word_order VARCHAR(20), Ancestor VARCHAR(20),PRIMARY KEY (Name), FOREIGN KEY (Ancestor) REFERENCES Language_family (Ancestor) ON DELETE SET NULL);
CREATE TABLE Dialect_Uses (Region VARCHAR(30),Ethnic_groups VARCHAR(20),Language_name VARCHAR(20),PRIMARY KEY (Region, Ethnic_groups, Language_name),FOREIGN KEY (Language_name) REFERENCES Language_ancestor (Name) ON DELETE CASCADE);
CREATE TABLE LexicalEntry_LexicalCategory (Lexical_category VARCHAR(20),Definition VARCHAR(1000),PRIMARY KEY (Definition));
CREATE TABLE LexicalEntry_Definition (LEID INTEGER,Definition VARCHAR(1000),Region VARCHAR(30) NOT NULL,Ethnic_groups VARCHAR(20) NOT NULL,Language_name VARCHAR(20) NOT NULL, PRIMARY KEY (LEID),FOREIGN KEY (Definition) REFERENCES LexicalEntry_LexicalCategory (Definition),FOREIGN KEY (Region, Ethnic_groups, Language_name) REFERENCES Dialect_Uses (Region, Ethnic_groups, Language_name) ON DELETE CASCADE);
CREATE TABLE ExampleSentence_Has (Sentence VARCHAR(1000),Source VARCHAR(50),LEID INTEGER NOT NULL,PRIMARY KEY (Sentence, Source),FOREIGN KEY (LEID) REFERENCES LexicalEntry_Definition (LEID) ON DELETE CASCADE);
CREATE TABLE WritingSystem (Name VARCHAR(20),Typology VARCHAR(20),PRIMARY KEY (Name));
CREATE TABLE OrthographicForm (Spelling VARCHAR(100), Num_characters INTEGER,PRIMARY KEY (Spelling));
CREATE TABLE Contains (SID INTEGER,IPA_transcription VARCHAR(20),PRIMARY KEY (SID, IPA_transcription), FOREIGN KEY (SID) REFERENCES IPASymbol (SID) ON DELETE CASCADE, FOREIGN KEY (IPA_transcription) REFERENCES Pronunciation (IPA_transcription) ON DELETE CASCADE);
CREATE TABLE Has (IPA_transcription VARCHAR(20),LEID INTEGER, PRIMARY KEY (IPA_transcription, LEID), FOREIGN KEY (IPA_transcription) REFERENCES Pronunciation (IPA_transcription) ON DELETE CASCADE, FOREIGN KEY (LEID) REFERENCES LexicalEntry_Definition (LEID) ON DELETE CASCADE);
CREATE TABLE UsedIn (SID INTEGER,Region VARCHAR(30),Ethnic_groups VARCHAR(20),Language_name VARCHAR(20),PRIMARY KEY (SID, Region, Ethnic_groups, Language_name),FOREIGN KEY (SID) REFERENCES IPASymbol (SID) ON DELETE CASCADE, FOREIGN KEY (Region, Ethnic_groups, Language_name) REFERENCES Dialect_Uses (Region, Ethnic_groups, Language_name) ON DELETE CASCADE);
CREATE TABLE Uses (Language_name VARCHAR(20),System_name VARCHAR(20),PRIMARY KEY (Language_name, System_name),FOREIGN KEY (Language_name) REFERENCES Language_ancestor (Name) ON DELETE CASCADE,FOREIGN KEY (System_name) REFERENCES WritingSystem (Name) ON DELETE CASCADE);
CREATE TABLE UsedBy (System_name VARCHAR(20),Spelling VARCHAR(50),PRIMARY KEY (System_name, Spelling), FOREIGN KEY (System_name) REFERENCES WritingSystem (Name) ON DELETE CASCADE, FOREIGN KEY (Spelling) REFERENCES OrthographicForm (Spelling) ON DELETE CASCADE);
CREATE TABLE Represents (LEID INTEGER,Spelling VARCHAR(100),Orthographic_community VARCHAR(20),PRIMARY KEY (LEID, Spelling),FOREIGN KEY (LEID) REFERENCES LexicalEntry_Definition (LEID) ON DELETE CASCADE, FOREIGN KEY (Spelling) REFERENCES OrthographicForm (Spelling) ON DELETE CASCADE);
